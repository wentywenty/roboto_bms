BmsProtocol::BmsProtocol(const std::string& port_name, int baud_rate)
    : serial_fd_(-1), port_name_(port_name), baud_rate_(baud_rate) {}

BmsProtocol::~BmsProtocol() { close_port(); }

bool BmsProtocol::open() {
    serial_fd_ = ::open(port_name_.c_str(), O_RDWR | O_NOCTTY | O_NDELAY);
    if (serial_fd_ < 0) return false;
    struct termios options;
    tcgetattr(serial_fd_, &options);
    speed_t baud = (baud_rate_ == 9600) ? B9600 : B115200;
    cfsetispeed(&options, baud);
    cfsetospeed(&options, baud);
    options.c_cflag |= (CLOCAL | CREAD | CS8);
    options.c_cflag &= ~(PARENB | CSTOPB | CSIZE);
    options.c_lflag &= ~(ICANON | ECHO | ECHOE | ISIG);
    options.c_oflag &= ~OPOST;
    tcsetattr(serial_fd_, TCSANOW, &options);
    return true;
}

void BmsProtocol::close_port() { if (serial_fd_ >= 0) { ::close(serial_fd_); serial_fd_ = -1; } }
bool BmsProtocol::is_open() const { return serial_fd_ >= 0; }

uint16_t BmsProtocol::calculate_crc(const uint8_t *data, size_t len) {
    uint8_t ucCRCHi = 0xFF, ucCRCLo = 0xFF;
    int iIndex;
    while(len--) {
        iIndex = ucCRCLo ^ *(data++);
        ucCRCLo = (uint8_t)(ucCRCHi ^ aucCRCHi[iIndex]);
        ucCRCHi = aucCRCLo[iIndex];
    }
    return (uint16_t)(ucCRCHi << 8 | ucCRCLo);
}

void BmsProtocol::send_read_request(uint16_t start_addr, uint16_t num_regs) {
    uint8_t frame[8] = {BMS_ADDR, FUNC_READ, (uint8_t)(start_addr >> 8), (uint8_t)start_addr, (uint8_t)(num_regs >> 8), (uint8_t)num_regs};
    uint16_t crc = calculate_crc(frame, 6);
    frame[6] = crc & 0xFF; frame[7] = crc >> 8;
    write(serial_fd_, frame, 8);
}

bool BmsProtocol::read_response(std::vector<uint8_t>& buffer, int expected_bytes) {
    buffer.assign(expected_bytes, 0);
    int total_read = 0;
    struct pollfd pfd = {serial_fd_, POLLIN, 0};
    while (total_read < expected_bytes) {
        if (poll(&pfd, 1, 150) > 0) {
            int n = read(serial_fd_, buffer.data() + total_read, expected_bytes - total_read);
            if (n > 0) total_read += n; else break;
        } else break;
    }
    if (total_read < 5) return false;
    uint16_t received_crc = buffer[total_read-2] | (buffer[total_read-1] << 8);
    uint16_t calc_crc = calculate_crc(buffer.data(), total_read - 2);
    if (received_crc != calc_crc) return false;
    return (total_read == expected_bytes);
}

bool BmsProtocol::read_basic_info(BatteryStatus& status) {
    tcflush(serial_fd_, TCIOFLUSH);
    send_read_request(0x9000, 15);
    std::vector<uint8_t> buf;
    if (read_response(buf, 35)) {
        status.voltage = get_u32(buf, 7) / 1000.0;
        status.current = get_i32(buf, 11) / 1000.0;
        status.temperature = static_cast<float>(get_u16(buf, 19)) - 40.0f;
        status.protect_status = get_u32(buf, 27);
        status.work_state = get_u16(buf, 3);
        return true;
    }
    return false;
}

bool BmsProtocol::read_capacity_info(BatteryStatus& status) {
    send_read_request(0x9028, 4);
    std::vector<uint8_t> buf;
    if (read_response(buf, 13)) {
        status.percentage = get_u16(buf, 3) / 100.0;
        status.charge = get_u32(buf, 7) / 1000.0;
        return true;
    }
    return false;
}

uint16_t BmsProtocol::get_u16(const uint8_t* buf, int offset) { return buf[offset] | (buf[offset+1] << 8); }
uint32_t BmsProtocol::get_u32(const uint8_t* buf, int offset) { return get_u16(buf, offset) | (get_u16(buf, offset + 2) << 16); }
int32_t BmsProtocol::get_i32(const uint8_t* buf, int offset) { return static_cast<int32_t>(get_u32(buf, offset)); }

uint16_t BmsProtocol::get_u16(const std::vector<uint8_t>& buf, int offset) { return get_u16(buf.data(), offset); }
uint32_t BmsProtocol::get_u32(const std::vector<uint8_t>& buf, int offset) { return get_u32(buf.data(), offset); }
int32_t BmsProtocol::get_i32(const std::vector<uint8_t>& buf, int offset) { return get_i32(buf.data(), offset); }

